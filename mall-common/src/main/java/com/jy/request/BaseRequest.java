package com.jy.request;

import java.io.Serializable;

/**
 * 基本请求
 */
public class BaseRequest {

    private int page;
    private int rows;

    public int getPage() {
        return page;
    }

    public void setPage(int page) {
        this.page = page;
    }

    public int getRows() {
        return rows;
    }

    public void setRows(int rows) {
        this.rows = rows;
    }
}
